const day = "Friday";

switch (day) {
  case "Monday":
    console.log("Monday: Start of the week.");
    break;
  case "Tuesday":
    console.log("Tuesday: Keep the momentum going.");
    break;
  case "Wednesday":
    console.log("Wednesday: Midweek communion Service.");
    break;
  case "Thursday":
    console.log("Thursday: Almost there.");
    break;
  case "Friday":
    console.log("Friday: Thank God it's Friday!");
    break;
  case "Saturday":
    console.log("Saturday: Time to relax.");
    break;
  case "Sunday":
    console.log("Sunday: Let's go to church and thank God.");
    break;
  default:
    console.log("Invalid day.");
}
